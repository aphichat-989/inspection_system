def get_language(request):
    language = request.session.get("ui_language") or request.COOKIES.get("ui_language") or DEFAULT_LANGUAGE
    return language if language in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE


def get_translations(language):
    return TRANSLATIONS.get(language, TRANSLATIONS[DEFAULT_LANGUAGE])


def context(request):
    language = get_language(request)
    return {
        "ui_language": language,
        "ui_languages": SUPPORTED_LANGUAGES,
        "ui_translations_json": json.dumps(get_translations(language), ensure_ascii=False),
    }
